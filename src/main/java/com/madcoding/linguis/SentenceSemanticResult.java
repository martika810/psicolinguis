package com.madcoding.linguis;

public class SentenceSemanticResult {
	private int totalNumWords = 0;
	private double averageLengthWord = 0;
	private int numAdverbs = 0;
	private int numNouns = 0;
	private int numAdjectives = 0;
	private int numVerbs = 0;
	private int numAbbr = 0;
	public int getTotalNumWords() {
		return totalNumWords;
	}
	public void setTotalNumWords(int totalNumWords) {
		this.totalNumWords = totalNumWords;
	}
	public double getAverageLengthWord() {
		return averageLengthWord;
	}
	public void setAverageLengthWord(double averageLengthWord) {
		this.averageLengthWord = averageLengthWord;
	}
	public int getNumAdverbs() {
		return numAdverbs;
	}
	public void setNumAdverbs(int numAdverbs) {
		this.numAdverbs = numAdverbs;
	}
	public int getNumNouns() {
		return numNouns;
	}
	public void setNumNouns(int numNouns) {
		this.numNouns = numNouns;
	}
	public int getNumAdjectives() {
		return numAdjectives;
	}
	public void setNumAdjectives(int numAdjectives) {
		this.numAdjectives = numAdjectives;
	}
	public int getNumVerbs() {
		return numVerbs;
	}
	public void setNumVerbs(int numVerbs) {
		this.numVerbs = numVerbs;
	}
	public int getNumAbbr() {
		return numAbbr;
	}
	public void setNumAbbr(int numAbbr) {
		this.numAbbr = numAbbr;
	}
	
	
	

}
